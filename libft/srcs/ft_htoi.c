#include "libft.h"

int		ft_htoi(char *s)
{
	int n;

	n = 0;
	while (*s)
	{
		n *= 16;
		if (*s >= 'A' && *s <= 'F')
			n += *s - 'A' + 10;
		else
			n += ft_isdigit(*s) ? *s - '0' : *s - 'a' + 10;
		++s;
	}
	return (n);
}
