#include "libft.h"

char	*ft_strcpy(char *dest, const char *src)
{
	unsigned int	index;

	index = 0;
	while (src[index])
	{
		dest[index] = src[index];
		index++;
	}
	dest[index] = '\0';
	return (dest);
}
