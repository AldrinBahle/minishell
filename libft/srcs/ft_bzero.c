#include "libft.h"

void	ft_bzero(void *str, size_t num_bts)
{
	char	*ptr;
	size_t	i;

	if (!num_bts)
		return ;
	ptr = str;
	i = 0;
	while (i < num_bts)
		*(ptr + i++) = 0;
}
